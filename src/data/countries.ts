// Helper function to get countries by continent
export const getCountriesByContinent = (continent: string): Country[] => {
  return countriesData.filter(country => country.continent === continent);
};

// Helper function to get country by ID
export const getCountryById = (id: string): Country | undefined => {
  return countriesData.find(country => country.id === id);
};

// Helper function to search countries
export const searchCountries = (query: string): Country[] => {
  const normalizedQuery = query.toLowerCase();
  return countriesData.filter(country => 
    country.name.toLowerCase().includes(normalizedQuery) ||
    country.aliases.some(alias => alias.toLowerCase().includes(normalizedQuery))
  );
};
